#include "libft.h"

char	*ft_strjoinfree(char *s1, char *s2, const char control)
{
	char	*out;

	out = ft_strjoin(s1, s2);
	if (control == 'L' || control == 'B')
		free(s1);
	if (control == 'R' || control == 'B')
		free(s2);
	return (out);
}